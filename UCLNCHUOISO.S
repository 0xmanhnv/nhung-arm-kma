	AREA RESET, DATA, READONLY
		DCD 0X20001000
		DCD START
ARR DCD 4,8,16,8		
	AREA MYCODE, CODE, READONLY
	ENTRY
	
START
SOPT EQU 4
	MOV R0,#1
	LDR R1,=SOPT
	LDR R2,=ARR
	LDR R3,[R2]
	ADD R0,#1
	ADD R2,#4
	LDR R4,[R2]
LOOP
	CMP R3,R4
	BGT	LON
	BLT	NHO
	BEQ	BANG
LON
	SUB R3,R4
	B LOOP
NHO
	SUB R4,R3
	B LOOP
BANG 
	MOV R3,R4
CHANGE
	ADD R0,#1
	ADD R2,#4
	LDR R4,[R2]
	CMP R0,R1
	BLE LOOP
KT

	END


	