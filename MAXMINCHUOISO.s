	AREA RESET, DATA, READONLY
	DCD 0X20001000
	DCD START_CODE
	
arMangSo4 dcd 1,2,4,-2,3,5,8,6,9

	AREA HUNGPT, CODE, READONLY
	ENTRY
	
START_CODE
SoPhanTu equ 9
	MOV R0, #1				
	LDR R1, =SoPhanTu		
	LDR R2, =arMangSo4
	LDR R4, [R2]
	LDR R5, [R2]
	
LON
	CMP R0, R1
	BEQ MIN
	ADD R0, #1
	ADD R2, #4
	LDR R3, [R2]
	CMP R3, R4
	BGT MAX
	BLE LON
NHO
	CMP R0, #1
	BEQ STOP
	SUB R0, #1
	SUB R2, #4
	LDR R3, [R2]
	CMP R3, R5
	BLT MIN
	BGT NHO
MIN
	MOV R5, R3
	B NHO
MAX
	MOV R4, R3
	B LON
STOP
	END
	