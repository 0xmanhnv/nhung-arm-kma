;TIM GIA TRI TONG CAC SO DUONG TRONG MA TRAN
	AREA RESET, DATA, READONLY
		DCD 0x20001000
		DCD START_CODE

ARNUM
	DCD 1, -2, 4
	DCD 3, -2, -3
	DCD 1,  0, -9
	
	AREA HUNGPT777, CODE, READONLY
		ENTRY
	
START_CODE
SOPT EQU 9
	MOV R0,#0
	MOV R1,#1
	LDR R2, =ARNUM
	LDR R3, =SOPT
	
CHECK
	LDR R4,[R2]
	CMP R4,#0
	BGE SODUONG
	BLT TIEPTUC

SODUONG
	ADD R0,R4
	B TIEPTUC

TIEPTUC
	ADD R1,#1
	ADD R2,#4
	CMP R1,R3
	BLE CHECK
	
	END