;DEM SO PHAN TU LA SO CHAN TREN DUONG CHEO PHU MA TRAN
	AREA RESET, DATA, READONLY
		DCD 0x20001000
		DCD START_CODE
ARNUM
	DCD 5,  2,  10, -8
	DCD 3, 10,  -4,  3
	DCD 4,  0,  20,  9
	DCD 1, -3, -10, -15

	AREA HUNGPT777, CODE, READONLY
		ENTRY
		
START_CODE
SOPT EQU 16
	MOV R0,#0
	MOV R1,#1
	LDR R2,=ARNUM
	LDR R3,=SOPT
	ADD R1,#3
	ADD R2,#12
	
CHECK
	LDR R4,[R2]
	MOV R5,R4
	CMP R5,#0
	BGE SODUONG
	BLT SOAM

SOAM
	ADD R5,#2
	CMP R5,#0
	BLT SOAM
	BGE SODUONG
	
SODUONG
	CMP R5,#2
	BLT NHOHON2
	BGE LONHON2

LONHON2
	SUB R5,#2
	CMP R5,#2
	BGE LONHON2
	BLT NHOHON2
	
NHOHON2
	CMP R5,#1
	BLT DEM
	BEQ TIEPTUC

DEM
	ADD R0,#1
	B TIEPTUC
	
TIEPTUC
	ADD R1,#3
	ADD R2,#12
	CMP R1,R3
	BLT CHECK
	
	END