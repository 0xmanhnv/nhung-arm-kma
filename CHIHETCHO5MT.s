;TINH TONG CAC SO CHIA HET CHO 5 TREN DUONG CHEO CHINH MA TRAN
	AREA RESET, DATA, READONLY
		DCD 0x20001000
		DCD START_CODE
		
ARNUM
	DCD 5,  2,  10,  1
	DCD 3, 10,   4,  3
	DCD 4,  0,  20,  9
	DCD 0, -3, -10, -15
	
	AREA HUNGPT777, CODE, READONLY
		ENTRY

START_CODE
SOPT EQU 16
	MOV R0,#0
	MOV R1,#1
	LDR R2, =ARNUM
	LDR R3, =SOPT
	
CHECK
	LDR R4,[R2]
	MOV R5,R4
	CMP R5,#0
	BGE SODUONG
	BLT SOAM
	
SOAM
	ADD R5,#5
	CMP R5,#0
	BLT SOAM
	BGE SODUONG

SODUONG
	CMP R5,#5
	BGE LONHON5
	BLT NHOHON5
	
LONHON5
	SUB R5,#5
	CMP R5,#5
	BGE LONHON5
	BLT NHOHON5
	
NHOHON5
	CMP R5,#0
	BEQ TONG
	B TIEPTUC
	
TONG
	ADD R0,R4
	B TIEPTUC

TIEPTUC
	ADD R1,#5
	ADD R2,#20
	CMP R1,R3
	BLE CHECK
	
	END