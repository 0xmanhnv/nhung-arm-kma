	AREA RESET, DATA, READONLY
		DCD 0X20001000
		DCD START
ARRMANG	DCD 1, 3, 5, 7, 9
	AREA MYCODE, CODE, READONLY
	
	ENTRY
	
START
SOPT	EQU 5

	MOV R0,#0
	MOV R1,#1
	LDR R2,=ARRMANG
	LDR R3,=SOPT
	MOV R5,#0
	MOV R6,#0
	
LOOP
	LDR R4,[R2]
	ADD R0,R4
	
	ADD R1,#1
	ADD R2,#4
	ADD R5,#1
	CMP R1,R3
	BLE LOOP
	
	MOV R7,R0
	
TBC
	SUB R0,R5
	ADD R6,#1
	CMP R0,#0
	BGT TBC
	
KT
	
	END
	
	