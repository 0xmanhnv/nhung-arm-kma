;TINH TONG CAC PHAN TU TREN DUONG CHEO CHINH MA TRAN
	AREA RESET, DATA, READONLY
		DCD 0x20001000
		DCD START_CODE

ARNUM
	DCD 5,  2,  10, -8
	DCD 3,  9,  -4,  3
	DCD 4,  0,   2,  9
	DCD 1, -3, -10, -15

	AREA HUNGPT777, CODE, READONLY
		ENTRY
		
START_CODE
SOPT EQU 16
	MOV R0,#0
	MOV R1,#1
	LDR R2,=ARNUM
	LDR R3,=SOPT
	
TINHTONG
	LDR R4,[R2]
	ADD R0,R4
	B TIEPTUC
	
TIEPTUC
	ADD R1,#5
	ADD R2,#20
	CMP R1,R3
	BLE TINHTONG
	
	END